#include "libft.h"

int	ft_isalnum(int c)
{
	if ((c <= 'Z' && c >= 'A') || (c <= 'z' && c >= 'a') || (c >= '0'
			&& c <= '9') || (c == '='))
		return (1);
	return (0);
}
